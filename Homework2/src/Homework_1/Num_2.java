package Homework_1;

/* 문제2) 문제 1번의 변수를 이용하여 각 연산을 수행한 결과를 출력하라.
2.1) 91 더하기 37
2.2) 91 빼기 37
2.3) 91 곱하기 37
2.4) 91 나누기 37 */

public class Num_2 {
	public static void main(String[] args) {
		
		int x = 37;
		int y = 91;
		
		System.out.println("y + x =  " + (y+x) );
		System.out.println("y - x =  " + (y-x) );
		System.out.println("y * x =  " + (y*x) );
		System.out.println("y / x =  " + (y/x) );
	}

}

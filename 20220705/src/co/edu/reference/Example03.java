package co.edu.reference;

public class Example03 {
	public static void main(String[] args) {
		// for 반복문을 활용 구구단 3단 출력
		for(int i= 1; i <= 9; i++) {
			System.out.println("3 * " + i + " = " + (3 * i));
		}
		
		
		
		for(int m=2; m<=9; m++) {
			for(int n=1; n<=9; n++) {
				System.out.println(m + "x" + n + " = " + (m*n));
			}
		}
		
		
		
		
		
		
	}

}
